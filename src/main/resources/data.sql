DROP TABLE IF EXISTS SUBSIDIARY;

CREATE TABLE SUBSIDIARY (
  ID INT AUTO_INCREMENT PRIMARY KEY,
  RUC VARCHAR(250) NOT NULL UNIQUE,
  NOMBRE VARCHAR(250) NOT NULL,
  TIPO VARCHAR(250) NOT NULL CHECK(TIPO IN ('CNB','SUCURSAL','AGENCIA')),
  DIRECCION VARCHAR(250) NOT NULL,
  GIRO_NEGOCIO VARCHAR(250),
  LATITUD VARCHAR(250),
  LONGITUD VARCHAR(250),
  PROPETARIO VARCHAR(250) NOT NULL
);

